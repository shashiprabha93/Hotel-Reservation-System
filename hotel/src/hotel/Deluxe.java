    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       
       try {
           String sql1="SELECT AvailableRooms from RoomType where RoomName= 'Deluxe' and Bed='Single' ";
           pst1=con.prepareStatement(sql1);
           rs1=pst1.executeQuery();
                     
           String sql2="SELECT AvailableRooms from RoomType where RoomName= 'Deluxe' and Bed='Double' ";
           pst2=con.prepareStatement(sql2);
           rs2=pst2.executeQuery();
           
           while(rs1.next()){
           
               int num1=rs1.getInt("AvailableRooms");
               String single=Integer.toString(num1);
               DelSingle.setText(single);
               
           }
           while(rs2.next()){
           
               int num2=rs2.getInt("AvailableRooms");
               String doble=Integer.toString(num2);
               DelDouble.setText(doble);
               
           }
           
           
       } catch (Exception e) {
           
       }   
    }//GEN-LAST:event_jButton1ActionPerformed

